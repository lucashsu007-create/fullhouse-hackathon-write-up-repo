"""
cfr_trained — Fullhouse test opponent (CFR-INSPIRED).

Preflop strategy is generated by training vanilla CFR (200k iterations) on a
simplified heads-up NLHE abstraction:
  - 5 hand-strength buckets weighted by realistic preflop frequency
  - Stylised action tree: fold / call / open(3bb) / 3bet(9bb) / 4bet(25bb) / jam
  - Pairwise equity modelled as logistic on bucket diff (premium ~80% vs strong)

The resulting STRATEGY table below contains MIXED strategies at many infosets
(e.g. bucket-2 BB facing open: 3-bet 26% / call 74%) — the CFR signature.
That's the point: this opponent has GENUINE balance at decision points, so a
hero that over-folds to 3-bets pays off the value, and a hero that calls down
light pays off the bluffs.

Caveats (the honest ones):
  - The abstraction is preflop-only; postflop here is a heuristic balanced
    strategy (similar in spirit to gto_balanced).
  - The 5-bucket card abstraction is coarse; real hands within a bucket play
    differently than the bucket average.
  - The opponent assumed during CFR training was itself (self-play), so the
    strategy is unexploitable in the abstract game but only approximately
    balanced in real 6-max NLHE.

Determinism: spot-seeded RNG samples actions from the CFR distribution, so
paired A/B tests stay deterministic.

Legal/safe: stdlib + optional eval7 only.
"""

