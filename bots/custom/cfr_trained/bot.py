# Map real hole cards → CFR bucket (0..4)
# ---------------------------------------------------------------------------

def _hand_bucket(cards):
    try:
        r1, s1 = cards[0][0], cards[0][1]
        r2, s2 = cards[1][0], cards[1][1]
    except (IndexError, TypeError):
        return 0
    v1, v2 = _VAL.get(r1, 0), _VAL.get(r2, 0)
    hi, lo = max(v1, v2), min(v1, v2)
    pair = (r1 == r2)
    suited = (s1 == s2)

    # bucket 4: premium (QQ+, AKs/AKo)
    if pair and hi >= 12:                          return 4
    if hi == 14 and lo == 13:                      return 4
    # bucket 3: strong (TT, JJ, AQ, AJs, KQs)
    if pair and hi >= 10:                          return 3
    if hi == 14 and lo == 12:                      return 3
    if hi == 14 and lo == 11 and suited:           return 3
    if hi == 13 and lo == 12 and suited:           return 3
    # bucket 2: playable (mid pairs, broadway, Axs)
    if pair and hi >= 7:                           return 2
    if hi == 14 and lo == 11:                      return 2
    if hi == 14 and lo == 10 and suited:           return 2
    if hi == 13 and lo == 12:                      return 2
    if hi == 13 and lo == 11 and suited:           return 2
    if hi == 12 and lo == 11 and suited:           return 2
    # bucket 1: speculative (small pairs, suited connectors, Axs)
    if pair:                                       return 1
    if suited and hi == 14:                        return 1
    if suited and (hi - lo) <= 2 and lo >= 5:      return 1
    # bucket 0: trash
    return 0


# ---------------------------------------------------------------------------
# Map real betting state → (cfr_player, cfr_history)
# CFR is HU, the real game is 6-max — we map to the NEAREST infoset.
# ---------------------------------------------------------------------------

def _cfr_context(state):
    """Return (cfr_player, cfr_history) for preflop lookup.

    cfr_player: 0 if we have not yet acted preflop, 1 otherwise (so the table's
    "BB role" responds to villain's prior raise).
    cfr_history: F/X/O/T/B/J string approximating the betting so far.
    """
    me = state.get("seat_to_act")
    log = state.get("action_log") or []

    # Count raises and detect whether we raised already
    n_raises = 0
    we_raised = False
    villain_raised_after_us = False
    we_acted = False
    for a in log:
        act = a.get("action")
        if act in ("raise", "all_in"):
            n_raises += 1
            if a.get("seat") == me:
                we_raised = True
            elif we_raised:
                villain_raised_after_us = True
        if a.get("seat") == me and act in ("raise", "call", "check", "all_in", "fold"):
            we_acted = True

    # Map raise count → CFR history surrogate
    # 0 raises = first to act; 1 = facing open; 2 = facing 3bet (we raised pre);
    # 3+ = facing 4bet
    if n_raises == 0:
        return (0, "")   # SB role, no action yet
    if n_raises == 1 and not we_raised:
        return (1, "O")  # BB role facing open
    if n_raises == 1 and we_raised:
        # Shouldn't happen — we raised but only 1 raise total means it was us
        return (0, "")
    if n_raises == 2 and we_raised:
        # We raised, they 3-bet us → SB role facing 'OT'
        return (0, "OT")
    if n_raises == 2 and not we_raised:
        # Open + 3bet from elsewhere, we're cold-calling decision
        return (1, "O")  # treat as if facing an open (conservative)
    if n_raises == 3:
        # Open + 3bet + 4bet; we're either the 3bettor facing 4bet, or original opener
        if we_raised and villain_raised_after_us:
            return (1, "OTB")  # we 3-bet, they 4-bet, we're responding
        return (1, "OTB")
    return (1, "O")


# ---------------------------------------------------------------------------
# Translate CFR action to real game action
# ---------------------------------------------------------------------------

def _bet_to(state, pot_fraction):
    pot = state.get("pot", 0)
    cur = state.get("current_bet", 0)
    my = state.get("your_bet_this_street", 0)
    stack = state.get("your_stack", 0)
    min_to = state.get("min_raise_to", 0)
    target = cur + int(pot * pot_fraction)
    cap = my + stack
    return min(max(target, min_to), cap)


def _cfr_action_to_real(cfr_action, state):
    """Translate F/X/O/T/B/J into a game action dict."""
    can_check = state.get("can_check", False)
    owed = state.get("amount_owed", 0)
    if cfr_action == "F":
        return {"action": "fold"} if owed > 0 else (
            {"action": "check"} if can_check else {"action": "fold"})
    if cfr_action == "X":
        return {"action": "check"} if (can_check and owed <= 0) else {"action": "call"}
    if cfr_action == "O":
        # Open: ~3bb if first in, or pot-sized otherwise
        return {"action": "raise", "amount": _bet_to(state, 1.0)}
    if cfr_action == "T":
        # 3-bet: 3x the current bet → pot-sized + a bit
        return {"action": "raise", "amount": _bet_to(state, 3.0)}
    if cfr_action == "B":
        # 4-bet: smaller relative pot bump
        return {"action": "raise", "amount": _bet_to(state, 2.2)}
    if cfr_action == "J":
        # Jam
        my = state.get("your_bet_this_street", 0)
        stack = state.get("your_stack", 0)
        return {"action": "all_in", "amount": my + stack}
    # Unknown action fallback
    return {"action": "fold"} if owed > 0 else {"action": "check"}


def _sample_cfr(infoset_key, rng):
    """Sample an action from the CFR strategy distribution at this infoset."""
    if infoset_key not in _STRATEGY:
        return None
    strat = _STRATEGY[infoset_key]
    items = list(strat.items())
    r = rng.random()
    cum = 0.0
    for action, prob in items:
        cum += prob
        if r <= cum:
            return action
    return items[-1][0]


