# Strategy
# ---------------------------------------------------------------------------

def _play(state):
    street = state.get("street", "preflop")
    rng = _spot_rng(state)

    if street == "preflop":
        hole = state.get("your_cards") or []
        bucket = _hand_bucket(hole)
        player, history = _cfr_context(state)
        key = f"{player}_{bucket}_{history or '_'}"
        cfr_action = _sample_cfr(key, rng)
        if cfr_action is None:
            # Fallback: simple TAG logic if no matching infoset
            can_check = state.get("can_check", False)
            owed = state.get("amount_owed", 0)
            if bucket >= 3:
                return {"action": "raise", "amount": _bet_to(state, 1.0)}
            if bucket >= 2 and owed > 0 and owed <= state.get("pot", 0) * 0.5:
                return {"action": "call"}
            return {"action": "check"} if can_check else {"action": "fold"}
        return _cfr_action_to_real(cfr_action, state)

    # POSTFLOP — balanced heuristic (CFR table is preflop-only)
    return _postflop_play(state, rng)


def decide(game_state):
    if isinstance(game_state, dict) and game_state.get("type") == "warmup":
        return {"action": "fold"}
    try:
        return _sanitize(_play(game_state), game_state)
    except Exception:
        return {"action": "fold"}
